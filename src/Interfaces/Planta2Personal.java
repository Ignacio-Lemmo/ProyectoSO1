    private void contratarBotones2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_contratarBotones2ActionPerformed
        if(Main.botones2Contratados < 15 && Main.botones2Contratados > 0 && (Main.botones2Contratados + Main.camaras2Contratados + Main.pantallas2Contratados + Main.pines2Contratados + Main.ensambladores2 < 15)){
            Main.botones2Contratados ++;
            Main.listaBotones2[Main.botones2Contratados-1] = new Botones2(Main.listaBotones2[0].botones2, Main.listaBotones2[0].botones2Mutex, Main.listaBotones2[0].ensambladorBotones2);
            Main.listaBotones2[Main.botones2Contratados-1].start();
            botones2.setText(String.valueOf(Main.botones2Contratados));
            Planta2.botones2.setText(String.valueOf(Main.botones2Contratados));

            CSVEL csvEscritor = new CSVEL();
            csvEscritor.EscribirCsv(); 
            
            if((Main.dias - Main.diasTranscurridos) > 5){
                Main.diasTranscurridos = Main.dias;
                Main.telefonos1Ensamblados = Main.producidos1Total;
                Main.gananciasNetas1 = Main.gananciaTotal1 - (Main.botones1Gastos+Main.camaras1Gastos+Main.pantallas1Gastos+Main.pines1Gastos+Main.gerente1Gastos+Main.jefe1Gastos);
                Main.telefonos2Ensamblados = Main.producidos2Total;
                Main.gananciasNetas2 = Main.gananciaTotal2- (Main.botones2Gastos+Main.camaras2Gastos+Main.pantallas2Gastos+Main.pines2Gastos+Main.gerente2Gastos+Main.jefe2Gastos);
                csvEscritor.GuardarHistorico(); 
                Main.cambio = true;
            }
            
         }else{
            JOptionPane.showMessageDialog(null, "La planta ha llegado al nÃºmero mÃ¡ximo de empleados.");
        }
    }//GEN-LAST:event_contratarBotones2ActionPerformed

